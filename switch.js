num = 100;

switch(num) {
    case 50:
        console.log("value is 50");
        break;
    case 100:
        console.log("value is 100");
        break;
    case 150:
        console.log("value is 150");
        break;
    default:
        //this block will run if no condition matches
        console.log('The number is not 50, 100, or 150');
        break;
 }